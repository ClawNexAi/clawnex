/**
 * alert-resolver.ts
 *
 * WHY: Converts a live AlertData + EvidencePayload into a safe TriageGraph
 * that the <TriageGraphCard> can render without ever surfacing raw payload
 * content, snippet data, or credentials in the UI layer.
 *
 * The function is intentionally pure — no I/O, no fetches, no side effects.
 * Every value in the output is derived from operator-summary-grade fields only.
 * Raw-payload access happens in dedicated RBAC-gated panels (Audit & Evidence,
 * Traffic Monitor) — not here.
 *
 * Resolver version: alert-resolver-v1
 */

import type { TabId } from "../types";
import type { AlertData, EvidencePayload } from "../types";
import type {
  TriageGraph,
  TriageStage,
  TriageArtifact,
  TriageStageId,
  TriageIssueSummary,
} from "./types";
import { TRIAGE_STAGE_ORDER } from "./types";
import { makeLastHoursFilter } from "./navigation";

// ---------------------------------------------------------------------------
// Public input contract
// ---------------------------------------------------------------------------

export interface ResolveAlertTriageInput {
  alert: AlertData;
  /**
   * Evidence payload returned by GET /api/alerts/:id/evidence.
   * Optional — alerts can exist before an evidence row is correlated.
   */
  evidence?: EvidencePayload | null;
  /**
   * "Now" reference for time-window filters. Defaults to new Date() so callers
   * do not need to pass it; tests can inject a fixed value for determinism.
   */
  now?: Date;
  /**
   * Caller's permission set. Reserved for future permission-gated artifacts
   * (e.g. "policies:write"). Not enforced in v1 — included for interface
   * stability so callers don't need a breaking change later.
   */
  permissions?: Set<string>;
}

