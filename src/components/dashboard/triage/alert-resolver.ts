/**
 * resolveAlertTriageGraph
 *
 * Converts a real AlertData + optional EvidencePayload into a safe TriageGraph.
 * Pure: no I/O, no fetch, no side effects.
 */
export function resolveAlertTriageGraph(input: ResolveAlertTriageInput): TriageGraph {
  const { alert, evidence, now = new Date() } = input;

  // 1. Map issue summary from safe alert metadata only
  const issue: TriageIssueSummary = buildIssueSummary(alert, evidence);

  // 2. Build all artifacts in canonical stage order
  const artifacts: TriageArtifact[] = [
    buildEvidenceArtifact(alert, evidence),
    buildSourceEventArtifact(alert, evidence),
    buildAffectedObjectArtifact(alert, evidence),
    buildRelatedActivityArtifact(alert, evidence, now),
    buildFixControlArtifact(alert, evidence),
  ];

  // 3. Build one stage per canonical stage ID, summarised from its artifacts
  const stages: TriageStage[] = TRIAGE_STAGE_ORDER.map((stageId) =>
    buildStage(stageId, artifacts),
  );

  // 4. Pick the default artifact: first resolved, then first non-restricted,
  //    then the first artifact unconditionally.
  const defaultArtifactId =
    artifacts.find((a) => a.state === "resolved")?.id ??
    artifacts.find((a) => a.state !== "restricted")?.id ??
    artifacts[0]?.id;

  return {
    issue,
    stages,
    artifacts,
    defaultArtifactId,
    generatedAt: now.toISOString(),
    resolverVersion: RESOLVER_VERSION,
  };
}

// ---------------------------------------------------------------------------
// Issue summary builder
// ---------------------------------------------------------------------------

function buildIssueSummary(
  alert: AlertData,
  evidence: EvidencePayload | null | undefined,
): TriageIssueSummary {
  // Derive a safe one-line summary from metadata only — no raw payload
  const summary = alert.description
    ? trimSafe(alert.description, 140)
    : `Alert from ${alert.source} — ${alert.severity} severity, status: ${alert.status ?? "open"}.`;

  // Build safe tags from known metadata fields
  const tags: TriageIssueSummary["tags"] = [];
  tags.push({ label: "source", value: alert.source, safe: true });

  // Rule key from evidence detections (first detection that has one)
  const ruleKey = evidence?.detections?.find((d) => d.rule_key)?.rule_key;
  if (ruleKey) {
    tags.push({ label: "rule_key", value: ruleKey, safe: true });
  }

  // Session ID from evidence (non-raw metadata field)
  if (evidence?.session_id) {
    tags.push({ label: "session", value: evidence.session_id, safe: true });
  }

  // Model from evidence (metadata — not payload content)
  if (evidence?.model) {
    tags.push({ label: "model", value: evidence.model, safe: true });
  }

  // Prompt hash is a hash — safe to surface as a tag (not the content)
  if (evidence?.prompt_hash) {
    tags.push({ label: "prompt_hash", value: evidence.prompt_hash, safe: true });
  }

  // Normalise severity to the union expected by TriageIssueSummary.
  // AlertData.severity is a plain string; cast to the union only if it matches.
  const knownSeverities = ["CRIT", "HIGH", "MED", "WARN", "LOW"] as const;
  type KnownSev = (typeof knownSeverities)[number];
  const severityUpper = alert.severity?.toUpperCase() as string;
  const severity: KnownSev | undefined = (knownSeverities as readonly string[]).includes(
    severityUpper,
  )
    ? (severityUpper as KnownSev)
    : undefined;

  return {
    id: alert.id,
    kind: "alert",
    title: alert.title,
    severity,
    status: alert.status ?? "open",
    source: alert.source,
    summary,
    tags,
    createdAt: alert.created_at,
    updatedAt: alert.updated_at,
  };
}

