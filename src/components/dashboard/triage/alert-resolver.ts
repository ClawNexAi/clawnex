const RESOLVER_VERSION = "alert-resolver-v1";

// ---------------------------------------------------------------------------
// Source-aware missing-state copy
//
// operator flagged 2026-05-07 looking at a session-watcher alert: the generic
// "No audit event correlated to this alert yet" copy hides the actual
// reason. session-watcher reads existing log files on disk; if the source
// session wasn't routed through the LiteLLM proxy at capture time, no
// audit row will EVER be correlated — that's accurate, not pending. Other
// sources have their own per-source semantics. These helpers return copy
// that tells the operator *why* a given stage is missing without leaking
// implementation detail.
// ---------------------------------------------------------------------------

function missingEvidenceReason(alert: AlertData): string {
  switch (alert.source) {
    case "session-watcher":
      return "This alert was generated by session-log scanning. No LiteLLM audit event is correlated because this session wasn't routed through the proxy at capture time.";
    case "shield":
      return "No audit row has been correlated yet — Shield can fire before the audit log writes for the same request.";
    case "correlation-engine":
      return "Correlation engine generated this from prior signals; no specific audit event is tied to it directly.";
    default:
      return "No audit event has been correlated to this alert yet.";
  }
}

function missingSourceEventReason(alert: AlertData): string {
  switch (alert.source) {
    case "session-watcher":
      return "Session-watcher reads from disk-stored OpenClaw session logs. The upstream LiteLLM proxy did not record this session, so no proxy traffic row exists.";
    case "shield":
      return "Proxy traffic row not yet correlated to this Shield event.";
    case "correlation-engine":
      return "Correlations fire across multiple events; there is no single proxy traffic row to link.";
    default:
      return "No proxy traffic row is linked to this alert yet.";
  }
}

function missingAffectedObjectReason(alert: AlertData): string {
  switch (alert.source) {
    case "session-watcher":
      return "This alert is bound to a session log file but no in-DB session_id has been resolved yet. Inspect the original log via Audit & Evidence.";
    case "correlation-engine":
      return "Correlation alerts span multiple sessions; no single affected object exists.";
    default:
      return "No session or object identifier is linked to this alert yet.";
  }
}

// ---------------------------------------------------------------------------
// session_id fallback — pull from alert.metadata JSON or description text
//
// operator flagged 2026-05-07: the resolver only read evidence.session_id, so
// the Affected Object stage stayed missing for any alert where evidence
// hadn't been fetched (or never will be — see missingEvidenceReason above).
// session-watcher writes the session UUID into alert.description as
// "Session: <uuid>"; some sources may also stuff it in alert.metadata as
// JSON. Try both before giving up.
//
// UUID regex matches the canonical 8-4-4-4-12 hex form. Other id shapes
// (numeric, short hashes) are intentionally not matched — adding them
// would risk false-positives that point operators at the wrong agent.
// ---------------------------------------------------------------------------

const UUID_PATTERN = /\b([0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12})\b/i;

function extractSessionIdFromAlert(alert: AlertData): string | null {
  // Try metadata JSON first — structured fields beat regex.
  if (alert.metadata) {
    try {
      const parsed = JSON.parse(alert.metadata);
      if (parsed && typeof parsed === "object") {
        const candidate =
          (parsed as Record<string, unknown>).session_id
          ?? (parsed as Record<string, unknown>).sessionId;
        if (typeof candidate === "string" && UUID_PATTERN.test(candidate)) {
          return candidate;
        }
      }
    } catch {
      // metadata isn't always JSON — fall through to description.
    }
  }
  // Description fallback — session-watcher pattern: "Session: <uuid>".
  if (alert.description) {
    const m = alert.description.match(/Session:\s*([0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12})/i);
    if (m) return m[1];
    // Generic fallback: any UUID anywhere in the description.
    const anyUuid = alert.description.match(UUID_PATTERN);
    if (anyUuid) return anyUuid[1];
  }
  return null;
}

