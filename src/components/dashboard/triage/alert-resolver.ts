/**
 * Evidence artifact (stage: evidence)
 *
 * Backed by the EvidencePayload.audit_event_id field.
 * State rules:
 *   resolved  — audit_event_id present + correlation_method "forward" (exact)
 *   derived   — audit_event_id present + correlation_method "fallback_nearest"
 *   missing   — no audit_event_id on the payload (or no payload at all)
 */
function buildEvidenceArtifact(
  alert: AlertData,
  evidence: EvidencePayload | null | undefined,
): TriageArtifact {
  const auditEventId = evidence?.audit_event_id ?? null;
  const correlationMethod = evidence?.correlation_method ?? null;

  // Determine state — "forward" is exact; "fallback_nearest" is heuristic
  const state = !auditEventId
    ? "missing"
    : correlationMethod === "forward"
      ? "resolved"
      : "derived";

  const shortLabel = auditEventId ? `Evidence · ${auditEventId}` : "Evidence";

  // Safe preview fields — metadata only, no payload content
  const previewFields: TriageArtifact["previewFields"] = [];
  if (auditEventId) {
    previewFields.push({ label: "audit_event_id", value: auditEventId, tone: "default" });
  }
  if (correlationMethod) {
    previewFields.push({
      label: "correlation_method",
      value: correlationMethod,
      tone: correlationMethod === "forward" ? "good" : "warn",
    });
  }
  if (evidence?.verdict) {
    previewFields.push({ label: "verdict", value: evidence.verdict, tone: "warn" });
  }
  if (evidence?.score != null) {
    previewFields.push({ label: "score", value: String(evidence.score), tone: "default" });
  }
  // Detection count — safe aggregate, not raw data
  if (evidence?.detections && evidence.detections.length > 0) {
    previewFields.push({
      label: "detections",
      value: `${evidence.detections.length} detection${evidence.detections.length !== 1 ? "s" : ""}`,
      tone: "muted",
    });
  }

  // Primary action — only when there is an audit_event_id to navigate to
  const primaryAction: TriageArtifact["primaryAction"] = auditEventId
    ? {
        tab: "auditEvidence" as TabId,
        opts: {
          id: auditEventId,
          highlight: auditEventId,
          fromAlert: alert.id,
          focusAlertId: alert.id,
          fromMissionControl: false,
        },
        label: "Open evidence ▸",
      }
    : undefined;

  return {
    id: `${alert.id}-evidence`,
    stageId: "evidence",
    label: shortLabel,
    shortLabel,
    kind: "evidence",
    state,
    confidence: state === "resolved" ? "exact" : state === "derived" ? "medium" : undefined,
    previewTitle: shortLabel,
    previewSummary:
      auditEventId
        ? "Correlated audit event. Raw sample and token data are available in Audit & Evidence under RBAC redaction."
        : missingEvidenceReason(alert),
    previewFields,
    primaryAction,
    reason: state === "missing" ? missingEvidenceReason(alert) : undefined,
    lastResolvedAt: state === "resolved" ? evidence?.audit_created_at : undefined,
    // Spec §10 amendment 2026-05-07: surface the first server-side-redacted
    // match-span snippet from EvidencePayload behind a default-collapsed
    // "Show match span" toggle (rendered in TriageArtifactPreview). The
    // snippet content here is the SAME pre-redacted data EvidenceInline
    // renders on Audit & Evidence — not raw payload. operator requested this
    // for "super-quick decision" triage UX. Only populated when the alert's
    // EvidencePayload has at least one matched-snippet entry.
    evidenceSnippet: pickFirstSnippet(evidence),
  };
}

/**
 * Pick the first matched-snippet entry from an EvidencePayload, if any.
 *
 * EvidencePayload.matched_snippets[] is the server-side-redacted match-span
 * array (Shield writes these AFTER masking PII; the bulk-payload field on
 * the same EvidencePayload remains forbidden in triage code). We surface
 * only the first entry — most alerts have one detection; multi-snippet
 * alerts route the operator to Audit & Evidence for the full list.
 */
function pickFirstSnippet(
  evidence: EvidencePayload | null | undefined,
): TriageArtifact["evidenceSnippet"] {
  if (!evidence) return undefined;
  // matched_snippets are added by the server alongside detections; some
  // alerts may have detections without matched-snippet capture.
  const list = evidence.matched_snippets;
  if (!list || list.length === 0) return undefined;
  const first = list[0];
  // Tolerate empty match strings — render won't display the snippet block
  // when match is falsy, so this is just a safety net.
  if (!first || typeof first.snippet_match !== "string") return undefined;
  return {
    before: first.snippet_before || undefined,
    match: first.snippet_match,
    after: first.snippet_after || undefined,
    ruleKey: first.rule_key || undefined,
  };
}

/**
 * Source Event artifact (stage: sourceEvent)
 *
 * Backed by EvidencePayload.proxy_traffic_id.
 * State: resolved if proxy_traffic_id exists, missing otherwise.
 */
function buildSourceEventArtifact(
  alert: AlertData,
  evidence: EvidencePayload | null | undefined,
): TriageArtifact {
  const proxyTrafficId = evidence?.proxy_traffic_id ?? null;
  const promptHash = evidence?.prompt_hash ?? null;

  const state: TriageArtifact["state"] = proxyTrafficId ? "resolved" : "missing";

  const shortLabel = proxyTrafficId
    ? `Source · ${proxyTrafficId}`
    : `Source · ${alert.source}`;

  // Safe preview fields — IDs and metadata only, never request/response content
  const previewFields: TriageArtifact["previewFields"] = [];
  if (proxyTrafficId) {
    previewFields.push({ label: "proxy_traffic_id", value: proxyTrafficId, tone: "default" });
  }
  if (evidence?.model) {
    previewFields.push({ label: "model", value: evidence.model, tone: "default" });
  }
  if (evidence?.provider) {
    previewFields.push({ label: "provider", value: evidence.provider, tone: "default" });
  }
  if (promptHash) {
    previewFields.push({ label: "prompt_hash", value: promptHash, tone: "default" });
  }
  if (evidence?.direction) {
    previewFields.push({ label: "direction", value: evidence.direction, tone: "default" });
  }
  if (evidence?.session_id) {
    previewFields.push({ label: "session_id", value: evidence.session_id, tone: "default" });
  }

  // Build NavigateOpts for traffic monitor
  // prompt_hash goes into filter.q only when present
  const trafficOpts = proxyTrafficId
    ? {
        highlight: proxyTrafficId,
        ...(promptHash ? { filter: { q: promptHash } } : {}),
        fromMissionControl: false,
      }
    : undefined;

  const primaryAction: TriageArtifact["primaryAction"] = proxyTrafficId
    ? {
        tab: "trafficMonitor" as TabId,
        opts: trafficOpts,
        label: "Open in Traffic ▸",
      }
    : undefined;

  return {
    id: `${alert.id}-source-event`,
    stageId: "sourceEvent",
    label: shortLabel,
    shortLabel,
    kind: "source",
    state,
    confidence: state === "resolved" ? "exact" : undefined,
    previewTitle: proxyTrafficId
      ? `Traffic Monitor · ${proxyTrafficId}`
      : `Source · ${alert.source}`,
    previewSummary: proxyTrafficId
      ? "Proxy-traffic row that generated the alert. Raw request/response content is available in Traffic Monitor under RBAC."
      : missingSourceEventReason(alert),
    previewFields,
    primaryAction,
    reason: state === "missing" ? missingSourceEventReason(alert) : undefined,
  };
}

/**
 * Affected Object artifact (stage: affectedObject)
 *
 * Uses session_id from evidence as the primary object ID. Falls back to
 * alert.id itself as a last resort so the stage is never blank.
 */
function buildAffectedObjectArtifact(
  alert: AlertData,
  evidence: EvidencePayload | null | undefined,
): TriageArtifact {
  // Prefer session_id from evidence (the structured field). Fall back to
  // parsing alert.metadata or alert.description (operator-flagged 2026-05-07:
  // session-watcher writes the session UUID into description as
  // "Session: <uuid>" — without this fallback the stage stayed missing
  // when evidence wasn't fetched / didn't carry session_id).
  const sessionId =
    evidence?.session_id
    ?? extractSessionIdFromAlert(alert)
    ?? null;
  const objectId = sessionId ?? null;
  // Track WHICH source the session_id came from so the preview can be
  // honest about confidence (evidence = exact; description regex = derived).
  const sessionFromEvidence = evidence?.session_id != null;

  const state: TriageArtifact["state"] = objectId
    ? sessionFromEvidence ? "resolved" : "derived"
    : "missing";

  const shortLabel = objectId ? `Object · ${objectId}` : `Object · ${alert.source}`;

  const previewFields: TriageArtifact["previewFields"] = [];
  if (sessionId) {
    previewFields.push({ label: "session_id", value: sessionId, tone: "default" });
    if (!sessionFromEvidence) {
      previewFields.push({
        label: "source",
        value: "alert.description",
        tone: "muted",
      });
    }
  }
  if (evidence?.model) {
    previewFields.push({ label: "model", value: evidence.model, tone: "default" });
  }
  if (evidence?.provider) {
    previewFields.push({ label: "provider", value: evidence.provider, tone: "default" });
  }
  // alert.status is safe metadata
  previewFields.push({ label: "alert_status", value: alert.status ?? "open", tone: "default" });

  const primaryAction: TriageArtifact["primaryAction"] = objectId
    ? {
        tab: "agents" as TabId,
        opts: { id: objectId, highlight: objectId },
        label: "Open agent ▸",
      }
    : undefined;

  return {
    id: `${alert.id}-object`,
    stageId: "affectedObject",
    label: shortLabel,
    shortLabel,
    kind: "object",
    state,
    confidence:
      state === "resolved" ? "exact"
      : state === "derived" ? "medium"
      : undefined,
    previewTitle: objectId
      ? `Agents & Sessions · ${objectId}`
      : `Affected Object · ${alert.source}`,
    previewSummary: objectId
      ? sessionFromEvidence
        ? "Session impacted by this alert. Drill in to inspect agent, model, and recent activity."
        : "Session ID extracted from the alert's description text — not yet correlated to a structured evidence row. Drill in to validate against the live agent state."
      : missingAffectedObjectReason(alert),
    previewFields,
    primaryAction,
    reason: state === "missing" ? missingAffectedObjectReason(alert) : undefined,
  };
}

/**
 * Related Activity artifact (stage: relatedActivity)
 *
 * Always state "derived" — this is a pre-built query, not a backing row.
 * Uses rule_key from the first detection with one, then alert source as fallback.
 */
function buildRelatedActivityArtifact(
  alert: AlertData,
  evidence: EvidencePayload | null | undefined,
  now: Date,
): TriageArtifact {
  // Rule key from evidence detections — first detection that has one
  const ruleKey = evidence?.detections?.find((d) => d.rule_key)?.rule_key ?? null;

  const shortLabel = ruleKey ? "Related · same-rule-24h" : "Related · same-source-24h";

  // Time window for the pre-built query
  const timeFilter = makeLastHoursFilter(24, now);

  // Build opts: min/max come from makeLastHoursFilter, q from rule_key if present
  const opts: NonNullable<TriageArtifact["primaryAction"]>["opts"] = {
    filter: {
      ...(ruleKey ? { q: ruleKey } : {}),
      source: [alert.source],
      min: timeFilter.min,
      max: timeFilter.max,
    },
  };

  const previewFields: TriageArtifact["previewFields"] = [];
  if (ruleKey) {
    previewFields.push({ label: "rule_key", value: ruleKey, tone: "default" });
  }
  previewFields.push({ label: "source", value: alert.source, tone: "default" });
  previewFields.push({ label: "window", value: "last 24h", tone: "default" });
  previewFields.push({ label: "confidence", value: "derived (query)", tone: "muted" });

  return {
    id: `${alert.id}-related`,
    stageId: "relatedActivity",
    label: shortLabel,
    shortLabel,
    kind: "related",
    state: "derived",
    confidence: "derived",
    previewTitle: ruleKey
      ? `Alerts · same rule · last 24h`
      : `Alerts · same source · last 24h`,
    previewSummary: ruleKey
      ? `Other alerts matching rule ${ruleKey} in the last 24 hours. Use this to gauge pattern prevalence and blast radius.`
      : `Other alerts from ${alert.source} in the last 24 hours.`,
    previewFields,
    primaryAction: {
      tab: "alertsIncidents" as TabId,
      opts,
      label: "Open related ▸",
    },
  };
}

/**
 * Fix / Control artifact (stage: fixControl)
 *
 * Points to the Policies & Rules configuration surface when a rule_key is known.
 * Falls back to a generic configuration target based on alert.source.
 * State: derived when a sensible target exists, missing when there is no
 * actionable control surface to point to.
 */
function buildFixControlArtifact(
  alert: AlertData,
  evidence: EvidencePayload | null | undefined,
): TriageArtifact {
  const ruleKey = evidence?.detections?.find((d) => d.rule_key)?.rule_key ?? null;

  // Determine target and state
  const hasRuleKey = ruleKey !== null;
  const state: TriageArtifact["state"] = "derived";

  const shortLabel = hasRuleKey
    ? `Fix · Policies & Rules`
    : `Fix · ${alert.source}`;

  const previewFields: TriageArtifact["previewFields"] = [];
  if (hasRuleKey) {
    previewFields.push({
      label: "target",
      value: "Configuration → Policies & Rules",
      tone: "default",
    });
    previewFields.push({ label: "control", value: ruleKey, tone: "default" });
    previewFields.push({
      label: "suggested_action",
      value: "Tune detection threshold or add a policy exception",
      tone: "default",
    });
  } else {
    previewFields.push({ label: "source", value: alert.source, tone: "default" });
    previewFields.push({ label: "target", value: "Configuration", tone: "muted" });
  }
  previewFields.push({ label: "permission", value: "policies:write", tone: "muted" });

  const primaryAction: TriageArtifact["primaryAction"] = {
    tab: "configuration" as TabId,
    opts: hasRuleKey
      ? { focus: "policiesAndRules", filter: { q: ruleKey }, fromMissionControl: false }
      : { focus: "policiesAndRules", fromMissionControl: false },
    label: "Open in Policies & Rules ▸",
  };

  return {
    id: `${alert.id}-fix-control`,
    stageId: "fixControl",
    label: shortLabel,
    shortLabel,
    kind: "fix",
    state,
    confidence: "derived",
    previewTitle: hasRuleKey
      ? "Configuration · Policies & Rules"
      : `Configuration · ${alert.source}`,
    previewSummary: hasRuleKey
      ? `Suppress similar matches, tune the rule, or escalate. Policies & Rules is the canonical control point for ${ruleKey}.`
      : `Configuration surface for ${alert.source}. Use Policies & Rules to adjust detection settings.`,
    previewFields,
    primaryAction,
    permission: "policies:write",
  };
}

// ---------------------------------------------------------------------------
// Stage builder
// ---------------------------------------------------------------------------

function buildStage(stageId: TriageStageId, artifacts: TriageArtifact[]): TriageStage {
  const stageArtifacts = artifacts.filter((a) => a.stageId === stageId);
  // Lead = first resolved artifact, else the first artifact in the stage
  const lead =
    stageArtifacts.find((a) => a.state === "resolved") ?? stageArtifacts[0];

  return {
    id: stageId,
    title: STAGE_TITLES[stageId],
    eyebrow: STAGE_EYEBROW[stageId],
    state: lead?.state ?? "missing",
    summary: lead ? deriveStageSummary(lead) : "Not yet resolved.",
    artifactIds: stageArtifacts.map((a) => a.id),
  };
}

/**
 * deriveStageSummary
 *
 * Pulls a one-line stage summary from an artifact. Uses previewSummary (already
 * safe) trimmed to 160 chars. Falls back to the reason string if present.
 */
function deriveStageSummary(artifact: TriageArtifact): string {
  if (artifact.previewSummary) {
    return trimSafe(artifact.previewSummary, 160);
  }
  if (artifact.reason) {
    return trimSafe(artifact.reason, 160);
  }
  return artifact.label;
}

